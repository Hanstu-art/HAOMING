// Evaluate Veg/Non-Veg dynamically
const processMenuData = () => {
    Object.keys(menuData).forEach(category => {
        menuData[category].forEach(item => {
            const name = item.name.toLowerCase();
            let veg = true;
            if (category === "Vegetable") {
                veg = true;
            } else if (category === "Chicken" || category === "Pork & Seafood") {
                veg = false;
            } else {
                if (name.includes("chicken") || name.includes("pork") || name.includes("seafood") ||
                    name.includes("prawn") || name.includes("fish") || name.includes("squid") || name.includes("meat") || name.includes("mixed")) {
                    veg = false;
                }
            }
            item.isVeg = veg;
        });
    });
};

document.addEventListener("DOMContentLoaded", () => {
    processMenuData();

    const tabsContainer = document.getElementById("menu-tabs");
    const container = document.getElementById("menu-items-container");
    const searchInput = document.getElementById("menu-search");
    const vegToggle = document.getElementById("veg-toggle");

    let currentCategory = Object.keys(menuData)[0];
    let searchQuery = "";
    let vegOnly = false;

    const renderMenu = () => {
        container.innerHTML = "";
        let items = [];

        if (searchQuery.trim() !== "") {
            Object.keys(menuData).forEach(cat => {
                const filtered = menuData[cat].filter(item =>
                    item.name.toLowerCase().includes(searchQuery.toLowerCase()) ||
                    (item.description && item.description.toLowerCase().includes(searchQuery.toLowerCase()))
                );
                items = items.concat(filtered);
            });
            document.querySelectorAll(".tab-btn").forEach(b => b.classList.remove("active"));
        } else {
            items = menuData[currentCategory];
            document.querySelectorAll(".tab-btn").forEach(b => {
                if (b.innerText === currentCategory) {
                    b.classList.add("active");
                } else {
                    b.classList.remove("active");
                }
            });
        }

        if (vegOnly) {
            items = items.filter(item => item.isVeg);
        }

        if (items.length === 0) {
            container.innerHTML = `<div class="no-results">No dishes found matching your criteria.</div>`;
            return;
        }

        let htmlContent = '<ol class="menu-ordered-list">\n';
        items.forEach(item => {
            const descriptionHTML = item.description ? `<div class="menu-item-desc">${item.description}</div>` : "";
            const badgeClass = item.isVeg ? "veg" : "non-veg";
            htmlContent += `
                <li class="menu-list-item">
                    <div class="menu-item-details">
                        <div class="menu-item-header">
                            <span class="dish-badge ${badgeClass}" title="${item.isVeg ? 'Vegetarian' : 'Non-Vegetarian'}"></span>
                            <h4 class="menu-item-name">${item.name}</h4>
                            <div class="menu-item-dots"></div>
                            <span class="menu-item-price">₹${item.price}</span>
                        </div>
                        ${descriptionHTML}
                    </div>
                </li>
            `;
        });
        htmlContent += '</ol>';
        container.innerHTML = htmlContent;
    };

    const categories = Object.keys(menuData);
    categories.forEach((category, index) => {
        const btn = document.createElement("button");
        btn.classList.add("tab-btn");
        if (index === 0) btn.classList.add("active");
        btn.innerText = category;

        btn.addEventListener("click", () => {
            currentCategory = category;
            searchQuery = "";
            if (searchInput) searchInput.value = "";

            container.style.opacity = 0;
            setTimeout(() => {
                renderMenu();
                container.style.opacity = 1;
            }, 200);
        });

        tabsContainer.appendChild(btn);
    });

    if (searchInput) {
        searchInput.addEventListener("input", (e) => {
            searchQuery = e.target.value;
            renderMenu();
        });
    }

    if (vegToggle) {
        vegToggle.addEventListener("change", (e) => {
            vegOnly = e.target.checked;

            const tabs = tabsContainer.querySelectorAll(".tab-btn");
            let activeHidden = false;
            let firstVisibleTab = null;

            tabs.forEach(tab => {
                const cat = tab.innerText;
                if (vegOnly && (cat === "Chicken" || cat === "Pork & Seafood")) {
                    tab.style.display = "none";
                    if (cat === currentCategory) {
                        activeHidden = true;
                    }
                } else {
                    tab.style.display = "";
                    if (!firstVisibleTab) firstVisibleTab = cat;
                }
            });

            if (activeHidden && firstVisibleTab) {
                currentCategory = firstVisibleTab;
                searchQuery = "";
                if (searchInput) searchInput.value = "";
            }

            container.style.opacity = 0;
            setTimeout(() => {
                renderMenu();
                container.style.opacity = 1;
            }, 200);
        });
    }

    renderMenu();
});
